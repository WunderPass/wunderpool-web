function resolveAddress(address) {
  const name = KNOWN_ADDRESSES[address.toLowerCase()];
  return name ? `${name} [${address}]` : address;
}

function formatParams(params) {
  return params.map((param) => {
    if (typeof param == 'string' && param.startsWith('0x'))
      return resolveAddress(param);
    if (typeof param == 'string') return param;
    try {
      return param.toString();
    } catch (error) {
      console.log(error);
      return param;
    }
  });
}

export default async function handler(req, res) {
  try {
    const { address, id, count, version = 5 } = req.query;
    const trxData = await fetchTransactionData(
      address,
      id,
      Number(count),
      version
    );
    const formattedData = trxData
      .filter((trx) => Boolean(trx))
      .map((trx) => {
        return {
          action: trx.action,
          contract: resolveAddress(trx.contractAddress),
          value: `${toEthString(trx.transactionValue, 18)} MATIC`,
          params: formatParams(decodeParams(trx.action, trx.params)),
        };
      });
    res.status(200).json(formattedData);
  } catch (error) {
    console.log(error);
    res.status(500).json({ error: `${error}` });
  }
}
